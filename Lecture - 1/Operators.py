print(10)
print(10 + 20)         # ADD
print(10 - 20)         # SUBTRACT
print(10 * 20)         # MULTIPLY
print(10 / 20)         # DIVIDE
print(10 // 20)        # FLOOR DIVISION (quotient without decimal)
print(10 % 20)         # MODULUS - REMAINDER
print(10 ^ 20)         # BITWISE XOR - returns 1 when bits are different
print(10 ** 20)        # POWER
print(20 > 10)         # GREATER THAN (True)
print(20 < 10)         # LESS THAN (False)
print(10 == 10)        # EQUAL TO - Comparison (True)
print(10 != 5)         # NOT EQUAL (True)
print(True and False)  # LOGICAL AND
print(True or False)   # LOGICAL OR
print(not True)        # LOGICAL NOT