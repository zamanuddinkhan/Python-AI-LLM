import random

from random import*

#random function -> generate random floating number between 0 to 1
for i in range(10):
    print(random())

#randint() function -> to generate random integer between two given numbers
for i in range(10):
    print(randint(1,100))

#uniform() ->returns random float between 2 given numbers
for i in range(10):
    print(uniform(1,100))
